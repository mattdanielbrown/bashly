## [@bashly-upgrade completions send_completions]
send_completions() {
  echo $'# cli completion                                           -*- shell-script -*-'
  echo $''
  echo $'# This bash completions script was generated by'
  echo $'# completely (https://github.com/bashly-framework/completely)'
  echo $'# Modifying it manually is not recommended'
  echo $''
  echo $'_cli_completions_node_flag_state() {'
  echo $'  case "$1:$2" in'
  echo $'    0:--help|0:-h) return 0 ;;'
  echo $'    0:--version|0:-v) return 0 ;;'
  echo $'    1:--help|1:-h) return 0 ;;'
  echo $'    2:--handler) return 2 ;;'
  echo $'    2:--help|2:-h) return 0 ;;'
  echo $'    2:--force|2:-f) return 0 ;;'
  echo $'    3:--user|3:-u) return 2 ;;'
  echo $'    3:--password|3:-p) return 2 ;;'
  echo $'    3:--help|3:-h) return 0 ;;'
  echo $'  esac'
  echo $''
  echo $'  return 1'
  echo $'}'
  echo $''
  echo $'_cli_completions_option_seen() {'
  echo $'  local completed_option option_name'
  echo $'  for completed_option in "${completed_options[@]}"; do'
  echo $'    for option_name in "$@"; do'
  echo $'      [[ "$completed_option" == "$option_name" ]] && return 0'
  echo $'    done'
  echo $'  done'
  echo $''
  echo $'  return 1'
  echo $'}'
  echo $''
  echo $'_cli_completions_resolve_node() {'
  echo $'  node_id=0'
  echo $'  node_word_count=0'
  echo $'  positional_index=0'
  echo $''
  echo $'  local word'
  echo $'  for word in "${non_options[@]}"; do'
  echo $'    case "$node_id:$word" in'
  echo $'      0:completions)'
  echo $'        node_id=1'
  echo $'        node_word_count=1'
  echo $'        ;;'
  echo $'      0:download)'
  echo $'        node_id=2'
  echo $'        node_word_count=1'
  echo $'        ;;'
  echo $'      0:d)'
  echo $'        node_id=2'
  echo $'        node_word_count=1'
  echo $'        ;;'
  echo $'      0:upload)'
  echo $'        node_id=3'
  echo $'        node_word_count=1'
  echo $'        ;;'
  echo $'      0:u)'
  echo $'        node_id=3'
  echo $'        node_word_count=1'
  echo $'        ;;'
  echo $'      *)'
  echo $'        break'
  echo $'        ;;'
  echo $'    esac'
  echo $'  done'
  echo $''
  echo $'  positional_index=$((${#non_options[@]} - node_word_count))'
  echo $'}'
  echo $''
  echo $'_cli_completions() {'
  echo $'  local cur=${COMP_WORDS[COMP_CWORD]}'
  echo $'  local prev='
  echo $'  if ((COMP_CWORD > 0)); then'
  echo $'    prev=${COMP_WORDS[$((COMP_CWORD - 1))]}'
  echo $'  fi'
  echo $''
  echo $'  local completed=()'
  echo $'  if ((COMP_CWORD > 1)); then'
  echo $'    completed=("${COMP_WORDS[@]:1:$((COMP_CWORD - 1))}")'
  echo $'  fi'
  echo $''
  echo $'  local non_options=()'
  echo $'  local completed_options=()'
  echo $'  local node_id='
  echo $'  local node_word_count=-1'
  echo $'  local positional_index=0'
  echo $'  local invalid_completion=0'
  echo $'  local flag_state=0'
  echo $'  _cli_completions_resolve_node'
  echo $''
  echo $'  local skip_next=0'
  echo $'  for word in "${completed[@]}"; do'
  echo $'    if ((skip_next)); then'
  echo $'      skip_next=0'
  echo $'      continue'
  echo $'    fi'
  echo $''
  echo $'    if [[ "${word:0:1}" == "-" ]]; then'
  echo $'      _cli_completions_node_flag_state "$node_id" "$word"'
  echo $'      flag_state=$?'
  echo $'      if (( flag_state == 1 )); then'
  echo $'        invalid_completion=1'
  echo $'        break'
  echo $'      fi'
  echo $''
  echo $'      completed_options+=("$word")'
  echo $'      if (( flag_state == 2 )); then'
  echo $'        skip_next=1'
  echo $'      fi'
  echo $'      continue'
  echo $'    fi'
  echo $''
  echo $'    non_options+=("$word")'
  echo $'    _cli_completions_resolve_node'
  echo $'  done'
  echo $''
  echo $'  COMPREPLY=()'
  echo $'  (( invalid_completion )) && return'
  echo $''
  echo $'  case "$node_id:$prev" in'
  echo $'    2:--handler)'
  echo $'      while read -r; do COMPREPLY+=("$REPLY"); done < <(compgen -W "curl wget" -- "$cur")'
  echo $'      return'
  echo $'      ;;'
  echo $'    3:--user|3:-u)'
  echo $'      while read -r; do COMPREPLY+=("$REPLY"); done < <(compgen -A user -- "$cur")'
  echo $'      return'
  echo $'      ;;'
  echo $'    3:--password|3:-p)'
  echo $'      return'
  echo $'      ;;'
  echo $'  esac'
  echo $''
  echo $'  if [[ "${cur:0:1}" != "-" ]] && (( positional_index == 0 )); then'
  echo $'    case "$node_id" in'
  echo $'      0)'
  echo $'        while read -r; do COMPREPLY+=("$REPLY"); done < <(compgen -W "completions download d upload u" -- "$cur")'
  echo $'        return'
  echo $'        ;;'
  echo $'    esac'
  echo $'  fi'
  echo $''
  echo $'  if [[ "${cur:0:1}" == "-" ]]; then'
  echo $'    case "$node_id" in'
  echo $'      0)'
  echo $'        local words=()'
  echo $'        _cli_completions_option_seen "--help" "-h" || words+=("--help" "-h")'
  echo $'        _cli_completions_option_seen "--version" "-v" || words+=("--version" "-v")'
  echo $'        while read -r; do COMPREPLY+=("$REPLY"); done < <(compgen -W "${words[*]}" -- "$cur")'
  echo $'        return'
  echo $'        ;;'
  echo $'      1)'
  echo $'        local words=()'
  echo $'        _cli_completions_option_seen "--help" "-h" || words+=("--help" "-h")'
  echo $'        while read -r; do COMPREPLY+=("$REPLY"); done < <(compgen -W "${words[*]}" -- "$cur")'
  echo $'        return'
  echo $'        ;;'
  echo $'      2)'
  echo $'        local words=()'
  echo $'        _cli_completions_option_seen "--help" "-h" || words+=("--help" "-h")'
  echo $'        _cli_completions_option_seen "--force" "-f" || words+=("--force" "-f")'
  echo $'        _cli_completions_option_seen "--handler" || words+=("--handler")'
  echo $'        while read -r; do COMPREPLY+=("$REPLY"); done < <(compgen -W "${words[*]}" -- "$cur")'
  echo $'        return'
  echo $'        ;;'
  echo $'      3)'
  echo $'        local words=()'
  echo $'        _cli_completions_option_seen "--help" "-h" || words+=("--help" "-h")'
  echo $'        _cli_completions_option_seen "--user" "-u" || words+=("--user" "-u")'
  echo $'        _cli_completions_option_seen "--password" "-p" || words+=("--password" "-p")'
  echo $'        while read -r; do COMPREPLY+=("$REPLY"); done < <(compgen -W "${words[*]}" -- "$cur")'
  echo $'        return'
  echo $'        ;;'
  echo $'    esac'
  echo $'  fi'
  echo $''
  echo $'  case "$node_id:$positional_index" in'
  echo $'    2:0)'
  echo $'      while read -r; do COMPREPLY+=("$REPLY"); done < <(compgen -A file -- "$cur")'
  echo $'      return'
  echo $'      ;;'
  echo $'    2:1)'
  echo $'      while read -r; do COMPREPLY+=("$REPLY"); done < <(compgen -A file -- "$cur")'
  echo $'      return'
  echo $'      ;;'
  echo $'    3:0)'
  echo $'      while read -r; do COMPREPLY+=("$REPLY"); done < <(compgen -W "README.md CHANGELOG.md" -- "$cur")'
  echo $'      return'
  echo $'      ;;'
  echo $'  esac'
  echo $'} &&'
  echo $'  complete -F _cli_completions cli'
  echo $''
  echo $'# ex: filetype=sh'
}